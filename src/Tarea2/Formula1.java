package Tarea2;

public class Formula1{
	private Escuderia[] escuderias;
	private Sesion[] sesiones;
	public Formula1(Escuderia[] escuderias, Sesion[] sesiones) {
		super();
		this.escuderias = escuderias;
		this.sesiones = sesiones;
	}
	public Escuderia[] getEscuderias() {
		return escuderias;
	}
	public void setEscuderias(Escuderia[] escuderias) {
		this.escuderias = escuderias;
	}
	public Sesion[] getSesiones() {
		return sesiones;
	}
	public void setSesiones(Sesion[] sesiones) {
		this.sesiones = sesiones;
	}
	
	
	
	
	
}